package QuestionBank;

public class Q10 {
    public static void main(String[] args) {
        int a = 035, b = 0x35, c = 35;
        System.out.printf("%d\n", a);
        System.out.printf("%d\n", b);
        System.out.printf("%d\n", c);
        /*
         * 8진수 35를 10진수로 변환:
         * 3 * 8^1 + 5 * 8^0 = 29
         * 즉 10진수로 29
         *
         * 16진수 35를 10진수로 변환:
         * 3 * 16^1 + 5 * 16^0 = 53
         * 즉 10진수로 53
         * */
        /*
         * 29
         * 53
         * 35
         * */
    }
}
