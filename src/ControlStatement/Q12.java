package ControlStatement;

public class Q12 {
    public static void main(String[] args) {
        int[][] aa = {
                {45, 50, 75},
                {89}
        };
        System.out.println(aa[0].length); // 3
        System.out.println(aa[1].length); // 1
        System.out.println(aa[0][0]); // 45
        System.out.println(aa[0][1]); // 50
        System.out.println(aa[1][0]); // 89
        /*
         * 3
         * 1
         * 45
         * 50
         * 89
         * */
    }
}
